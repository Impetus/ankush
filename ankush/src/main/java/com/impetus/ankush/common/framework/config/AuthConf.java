package com.impetus.ankush.common.framework.config;

import org.codehaus.jackson.annotate.JsonIgnore;


/**
 * The Class AuthConf.
 *
 * @author hokam
 */
public class AuthConf  implements Configuration {

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;
	
	/** The username. */
	private String username;
	
	/** The password. */
	private String password;
	
	/** The type. */
	private String type;

	/**
	 * Gets the username.
	 *
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}

	/**
	 * Sets the username.
	 *
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}

	/**
	 * Gets the password.
	 *
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * Sets the password.
	 *
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * Sets the type.
	 *
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * Checks if is auth type password.
	 *
	 * @return true, if is auth type password
	 */
	@JsonIgnore
	public boolean isAuthTypePassword() {
		if(type == null) {
			return false;
		}
		
		return type.equals("password");
	}
}
